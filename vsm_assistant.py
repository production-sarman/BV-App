def extract_stage(text: str) -> Optional[str]:
    m = re.search(r"\b(\d+\.\d+)\b", text)
    return m.group(1) if m else None

def find_variant(query: str):
    """
    Match the query to a variant using TOKEN OVERLAP SCORING.

    OLD approach (broken):
        if _norm(variant) in _norm(query)  ← "C" matches inside "TR_3PH_CS_01_6V_2000A"

    NEW approach (correct):
        Split both variant and query into tokens, count how many match.
        Pick the variant with the HIGHEST score and at least 2 tokens matched.
        Short/ambiguous variants like "C" will always lose to the real full code.

    Example:
        query   = "TR_3PH_CS_01_6V_2000A 1.3"
        tokens  = {"TR", "3PH", "CS", "01", "6V", "2000A"}

        variant "C"                    → score 0  (no token overlap)
        variant "TR_3PH_CS_01_6V_2000A" → score 6  (wins correctly)
    """
    query_tokens = set(_norm(query).split("_"))
    # Remove pure stage-number tokens like "1" "3" "1.3" from scoring
    query_tokens = {t for t in query_tokens if not re.match(r"^\d+(\.\d+)?$", t)}

    best_score   = 0
    best_variant = None
    best_ptype   = None

    for ptype, pdata in central_index.items():
        for v in pdata.get("variants", {}):
            variant_tokens = set(_norm(v).split("_"))
            score = len(variant_tokens & query_tokens)
            if score > best_score:
                best_score   = score
                best_variant = v
                best_ptype   = ptype

    # Require at least 2 tokens to match — prevents single-letter variants
    # like "C" from ever winning
    if best_score >= 2:
        return best_ptype, best_variant

    return None, None

