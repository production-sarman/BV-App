# HELPERS
# ==========================
def get_file_ext(path: str) -> str:
    return os.path.splitext(path)[1].lower()

def get_mime_type(path: str) -> str:
    mime, _ = SUPPORTED_FILE_TYPES.get(get_file_ext(path), ("application/octet-stream", False))
    return mime

def is_previewable(path: str) -> bool:
    _, previewable = SUPPORTED_FILE_TYPES.get(get_file_ext(path), ("application/octet-stream", False))
    return previewable

def is_supported(path: str) -> bool:
    return get_file_ext(path) in SUPPORTED_FILE_TYPES

def resolve_path(stored_path: str) -> str:
    """
    Convert a path from central_index.json into a real absolute path.

    Handles both cases:
      1. RELATIVE path (new format): "Master_Documents/EE/.../file.docx"
         -> joins with VSM_ROOT to get absolute path on current drive
      2. ABSOLUTE path (old format): "D:\\vector_database\\VSM\\..."
         -> returned as-is (still works if files are on that drive)

    This means old central_index.json files keep working, and new ones
    are fully portable across drives.
    """
    if os.path.isabs(stored_path):
        # Old absolute path format — use directly
        return stored_path
    else:
        # New relative path format — resolve against vsm_root
        return os.path.normpath(os.path.join(VSM_ROOT, stored_path))

def pick_best_file(documents: list) -> Optional[str]:
    existing = [resolve_path(p) for p in documents if os.path.exists(resolve_path(p))]
    if not existing:
        return None
    for ext in FILE_PRIORITY:
        for p in existing:
            if get_file_ext(p) == ext:
                return p
    return existing[0]

def read_stage_explanation(stage: str) -> str:
    path = os.path.join(VSM_DATA_FOLDER, f"{stage}_explanation.docx")
    if not os.path.exists(path):
        return f"No explanation available for stage {stage}"
    doc = Document(path)
    return "\n".join(p.text for p in doc.paragraphs if p.text.strip())

def create_versioned_copy(source_path: str) -> str:
    """Copy a single file into Working_Copies with a version suffix."""
    date_folder = os.path.join(WORKING_COPIES, datetime.now().strftime("%Y-%m-%d"))
    os.makedirs(date_folder, exist_ok=True)
    base = os.path.basename(source_path)
    name, ext = os.path.splitext(base)
    version = 1
    while True:
        dest = os.path.join(date_folder, f"{name}_v{version}{ext}")
        if not os.path.exists(dest):
            break
        version += 1
    shutil.copy2(source_path, dest)
    return dest

def _norm(text: str) -> str:
    return re.sub(r"[\s\-_]+", "_", text.strip().upper())

# ==========================
# IN-MEMORY ZIP STREAM
# ─────────────────────────
# Used ONLY for stage 2.4 folder downloads.
# Entire zip is built in RAM and streamed in 1MB chunks.
# Nothing written to disk — zero disk usage.
# ZIP_STORED used (no compression) because source files
# (.docx, .pdf, .sldprt etc.) are already compressed formats.
# ==========================
def zip_folder_stream(folder_path: str):
    buffer = io.BytesIO()
    with zipfile.ZipFile(buffer, "w", zipfile.ZIP_STORED) as zf:
        for root, dirs, files in os.walk(folder_path):
            for fname in files:
                fpath   = os.path.join(root, fname)
                arcname = os.path.relpath(fpath, folder_path)
                zf.write(fpath, arcname=arcname)
    buffer.seek(0)
    while True:
        chunk = buffer.read(1024 * 1024)  # 1MB chunks
        if not chunk:
            break
        yield chunk

