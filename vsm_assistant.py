# Flutter opens this URL in the browser (same as download_url for 1.3).
# Browser hits this GET endpoint → server streams zip from memory → 
# browser automatically downloads the zip file.
# Zero disk usage — built and streamed entirely in RAM.
# ==========================
@router.get("/download_zip/")
def download_zip(folder_path: str, zip_name: str):
    # Security check:
    # folder_path must contain "Master_Documents" somewhere in it —
    # this handles cases where code runs on C: but database is on D:
    # or any other drive, without breaking the path validation.
    abs_folder = os.path.abspath(folder_path)

    # Normalize to forward slashes for consistent checking
    # This single line handles the conversion correctly
    norm_folder = abs_folder.replace("\\", "/")

    if "Master_Documents" not in norm_folder:
        raise HTTPException(status_code=403, detail="Access denied — path must be inside Master_Documents")
    if not os.path.isdir(abs_folder):
        raise HTTPException(status_code=404, detail=f"Folder not found: {abs_folder}")

    safe_name = re.sub(r"[^\w\-.]", "_", zip_name)
    print(f"  [DOWNLOAD_ZIP] Streaming zip: {safe_name}.zip  folder: {abs_folder}")

    return StreamingResponse(
        zip_folder_stream(abs_folder),
        media_type="application/zip",
        headers={
            "Content-Disposition": f"attachment; filename={safe_name}.zip",
        },
    )


