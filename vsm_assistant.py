@router.post("/query")
def process_query(req: QueryRequest, request: Request):

    user_input = req.query.strip()
    stage      = extract_stage(user_input)

    if not stage:
        raise HTTPException(
            status_code=400,
            detail=(
                "Could not find a stage number in your query. "
                "Try: '1.3 for EE_01_03_10-400A_440-440V' or '2.4 Linear Fixture Pneumatic'"
            )
        )

    product_type, variant_code = find_variant(user_input)

    if not variant_code:
        all_variants = [
            vk
            for pd in central_index.values()
            for vk in pd.get("variants", {}).keys()
        ]
        raise HTTPException(
            status_code=404,
            detail=(
                f"Could not match any product from your query '{user_input}'. "
                f"Known products: {all_variants}"
            )
        )

    explanation = read_stage_explanation(stage)

    # ==========================
    # STEP 1 — Scan Master_Documents for best matching item
    # ─────────────────────────────────────────────────────
    # Stage 1.3 → always a single file  → serve directly (NO zip)
    # Stage 2.4 → can be a folder       → stream as in-memory zip
    # Any stage → single file match     → serve directly (NO zip)
    # ==========================
    product_root = os.path.join(MASTER_DOCS, product_type)
    variant_root = None

    if os.path.exists(product_root):
        for folder in os.listdir(product_root):
            if _norm(folder) == _norm(variant_code):
                variant_root = os.path.join(product_root, folder)
                break

    if variant_root and os.path.isdir(variant_root):

        query_tokens = set(_norm(user_input).split("_"))
        query_tokens = {t for t in query_tokens if not re.match(r"^\d+\.\d+$", t)}

        best_match = None
        best_score = 0

        for item in os.listdir(variant_root):
            item_path = os.path.join(variant_root, item)
            tokens    = set(_norm(item).split("_"))
            score     = len(tokens & query_tokens)
            if score > best_score:
                best_score = score
                best_match = (item, item_path)

        if best_match and best_score >= 2:
            item_name, item_path = best_match

            # ── FOLDER match → only valid for stage 2.4 ──
            if os.path.isdir(item_path):
                if stage == "2.4":
                    # Return download_url just like stage 1.3.
                    # Flutter opens this in the browser → browser auto-downloads the zip.
                    # The /vsm/download_zip/ endpoint streams it live from memory.
                    safe_name    = re.sub(r"[^\w\-.]", "_", item_name)
                    from urllib.parse import quote
                    encoded_path = quote(item_path.replace("\\", "/"), safe=":/")
                    print(f"  [2.4] Returning download_url for zip: {safe_name}")
                    return {
                        "stage":              stage,
                        "product_code":       variant_code,
                        "matched_from_query": user_input,
                        "explanation":        explanation,
                        "download_url":       f"/download_zip/?folder_path={encoded_path}&zip_name={safe_name}",
                        "preview_url":        "",
                        "file_type":          ".zip",
                        "entry_type":         "folder_zip",
                    }
                else:
                    return {
                        "stage":        stage,
                        "product_code": variant_code,
                        "explanation":  explanation,
                        "error":        f"Stage {stage} matched a folder — only stage 2.4 supports folder downloads.",
                        "download_url": "",
                        "preview_url":  "",
                    }

            # ── FILE match → serve directly, no zip ──
            # This is the normal path for stage 1.3 (single .docx/.pdf)
            if os.path.isfile(item_path) and is_supported(item_path):
                working_path  = create_versioned_copy(item_path)
                relative_path = os.path.relpath(working_path, WORKING_COPIES).replace("\\", "/")
                ext           = get_file_ext(item_path)
                return {
                    "stage":              stage,
                    "product_code":       variant_code,
                    "matched_from_query": user_input,
                    "explanation":        explanation,
                    "download_url":       f"/download_doc/?relative_path={relative_path}",
                    "preview_url":        (
                        f"/view_doc/?relative_path={relative_path}"
                        if is_previewable(item_path) else ""
                    ),
                    "file_type":  ext,
                    "entry_type": "file",
                }

    # ==========================
    # STEP 2 — Fallback: central_index lookup
    # resolve_path() handles both old absolute paths and new relative paths
    # ==========================
    variant_data     = central_index[product_type]["variants"].get(variant_code, {})
    available_stages = list(variant_data.keys())

    if stage not in variant_data:
        return {
            "stage":            stage,
            "product_code":     variant_code,
            "explanation":      f"Stage {stage} not available. Available: {available_stages}",
            "download_url":     "",
            "preview_url":      "",
            "available_stages": available_stages,
            "file_type":        None,
        }

    stage_data = variant_data[stage]
    docs = stage_data.get("documents", [])
    if isinstance(docs, str):
        docs = [docs]

    best_path = pick_best_file(docs)

    if not best_path:
        return {
            "stage":            stage,
            "product_code":     variant_code,
            "explanation":      explanation,
            "download_url":     "",
            "preview_url":      "",
            "available_stages": available_stages,
            "file_type":        None,
            "error":            "No files found on disk for this stage.",
        }

    if not is_supported(best_path):
        return {
            "stage":            stage,
            "product_code":     variant_code,
            "explanation":      explanation,
            "download_url":     "",
            "preview_url":      "",
            "available_stages": available_stages,
            "file_type":        get_file_ext(best_path),
            "error":            f"Unsupported file type: {get_file_ext(best_path)}",
        }

    working_path  = create_versioned_copy(best_path)
    relative_path = os.path.relpath(working_path, WORKING_COPIES).replace("\\", "/")
    ext           = get_file_ext(best_path)

    return {
        "stage":              stage,
        "product_code":       variant_code,
        "matched_from_query": user_input,
        "explanation":        explanation,
        "download_url":       f"/download_doc/?relative_path={relative_path}",
        "preview_url":        (
            f"/view_doc/?relative_path={relative_path}"
            if is_previewable(best_path) else ""
        ),
        "available_stages": available_stages,
        "file_type":        ext,
        "all_file_types":   [get_file_ext(resolve_path(p)) for p in docs if os.path.exists(resolve_path(p))],
        "entry_type":       "file",
    }


